/* Addition */
console.log(10 + 20);           // => 30

/* Concatenation */
console.log(10 + "Yousif");     // => 10Yousif

/* Not a number */
console.log(10 - "Yousif");    // NaN

/* Type of NaN is number */
console.log(typeof NaN);

/* Division */
console.log(10 / 2);

/* Multiplication */
console.log(10 * 2);

/* Exponentiation => ES7 */
console.log(10 ** 2);


/* Modulus */
console.log(10 % 2);
console.log(8 % 3);

/* Increment [Post / Pre] */
var num1 = 5, num2 = 6;
console.log(num1++);    // Post Increment   => 5
console.log(++num2);    // Pre Increment    => 7

/* Decrement [Post / Pre] */
var num1 = 5, num2 = 6;
console.log(num1--);    // Post Increment   => 5
console.log(--num2);    // Pre Increment    => 5