/* Operator to get the type of the data => typeof */
console.log(typeof("Yousif Adel With Double Quotes"));
console.log(typeof "Yousif Adel With Double Quotes");


/* 1. String  */
console.log("Yousif Adel With Double Quotes");
console.log('Yousif Adel With Single Quotes');

/* 2. Number */
console.log(typeof 500);
console.log(500);

/* 3. Array => Object */
console.log(typeof [10, 20, 30]);
console.log([10, 20, 30]);
console.log(typeof ['sh', 'sh', 'sh']);
console.log(['sh', 'sh', 'sh']);

/* 4. Object & its type is object */
console.log({name: "Yousif", age: 22, country: "Egypt"});
console.log(typeof {name: "Yousif", age: 22, country: "Egypt"});

/* 5. Boolean */
console.log(typeof true);
console.log(true);
console.log(false);

/* 6. Undifined  */
console.log(undefined);
console.log(typeof undefined);

/* 7. null => Object */
console.log(null);
console.log(typeof null);