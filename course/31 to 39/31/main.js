/* 

Comparison Operators
********************

    ==      Equal       [Value]
    !=      Not Equal   [Value]
    
    ===     Identical       [Value + type]
    !==     Not Identical   [Value + type]

    <       Smaller
    <=      Smaller than or Equal
    >       Larger
    >=      Larger than or Equal
    */

console.log(10 == "10");
console.log(10 != "10");

console.log(10 === "10");
console.log(10 !== "10");

console.log(10 > 20);
console.log(10 < 20);
console.log(10 <= 10);
console.log(10 >= 10);

