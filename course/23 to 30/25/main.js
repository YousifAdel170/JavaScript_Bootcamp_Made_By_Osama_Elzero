/* Math Object */


/* 1. round() */
console.log(Math.round(99.2));
console.log(Math.round(99.5));

/* 2. ceil() */
console.log(Math.ceil(99.2));
console.log(Math.ceil(99.5));

/* 3. floor() */
console.log(Math.floor(99.2));
console.log(Math.floor(99.5));

/* 4. min()  */
console.log(Math.min(1, 2, 3, 0, -50, 90));

/* 5. max()  */
console.log(Math.max(1, 2, 3, 0, -50, 90));

/* 6. pow(num, power)  */
console.log(Math.pow(2, 4));

/* 7. random() */
console.log(Math.random());

/* 8. trunc() [ES6]  => get the integer number only */
console.log(Math.trunc(100.59));
console.log(Math.trunc(99.2));
console.log(Math.trunc(99.5));